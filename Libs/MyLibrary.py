class MyLibrary:
    def greet(self, name):
        """Greets the person with the given name."""
        return f"Hello, {name}!"

    def add_numbers(self, a, b):
        """Adds two numbers and returns the result."""
        return int(a) + int(b)

    def multiply_numbers(self, a, b):
        """Multiplies two numbers and returns the result."""
        return int(a) * int(b)